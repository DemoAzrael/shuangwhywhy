<script>
    (function($){
        var l =["跟随仙剑六一起御剑飞行吧~~","江流儿小时候也挺可爱的~~","但丁来告诉你什么是狂拽酷炫~"];
        var swiper = new Swiper('.swiper-container', {
            pagination: '.swiper-pagination',
            paginationClickable: true,
            loop:true,
            autoplay : 5000,
            autoplayDisableOnInteraction : false,
            onSlideChangeEnd: function(swiper){
                var index = swiper.activeIndex-1;
                if(index==3){
                    index =0;
                }
                $('.swiper-container .zhezhao').html(l[index]);
            }
        });
        var  height = $(window).height();
        $('.one .zhezhao_1').css({
            "height":height
        });
        //ajax 调用图片
        var url = "api/index_c_p.php";
        var arr =["cartoon",'movie','game','music'];
        var j = 0;
        var arr_m = [];
        function ajax_picture() {
            if(j<4) {
                var that = $('.content .one .message').eq(j).find('.pic');
                $.ajax({
                    url: url,
                    type: "POST",
                    data: {
                        type:arr[j]
                    },
                    success: function (json) {
                        var data = JSON.parse(json);
                        for (var i = 0; i < 6; i++) {
                            var name = data[i][2];
                            var src = data[i][3];
                            var shotM = data[i][4];
                            arr_m.push(data[i][5]);
                            that.eq(i).find('img').attr({
                                'src': src
                            });
                            that.eq(i).find('p').html(name);
                            that.eq(i).find('.introduce').html(shotM);
                        }
                        j++;
                        ajax_picture();
                    }
                });
            }else{
                //点击图片出现遮罩层显示介绍信息
                var a = function (e) { e.preventDefault(); }
                $('.one .message a').click(function(){
                    $('.one .zhezhao_1').show();
                    var mess = $(this).find('.pic').attr('data-index');
                    var src = $(this).find('img').attr('src');
                    var na = $(this).find('p').eq(0).html();

                    $('.one .zhezhao_1 .m_h').attr({
                        "src":src
                    });
                    $('.one .zhezhao_1 h2').html(na);
                    $('.one .zhezhao_1 p').html(arr_m[mess]);
                    document.addEventListener('touchmove',a,false);//禁止屏幕滚动
                });
                $('.one .zhezhao_1 .delete').click(function(){
                    $('.one .zhezhao_1').hide();
                    document.removeEventListener('touchmove',a,false);//恢复屏幕滚动
                });
            }
        }
        ajax_picture();
    })(jQuery);
</script>